# Programın sürekli çalışması için sonsuz döngü başlatıyoruz
while True:
    print("\n" + "="*40)
    print("             ANA MENÜ")
    print("="*40)
    print("1 - Namazları öğrenmek")
    print("2 - Sureleri öğrenmek")
    print("3 - Duaları öğrenmek (Henüz eklenmedi)")
    print("4 - Esmaül Hüsna'ları öğrenmek (Henüz eklenmedi)")
    print("0 - Programdan Çıkış")
    print("="*40)
    
    secim = input("Lütfen yapmak istediğiniz işlemin numarasını giriniz: ")
    
    if secim == "1":
        print("\n--- NAMAZ REKATLARI ---")
        print("Sabah Namazı toplam 4 rekatdır. 2 Rekat Sünnet 2 Rekat Farz'dır.")
        print("Öğle Namazı toplam 10 rekatdır. 4 Rekat Sünnet 4 Farz ve 2 Rekat Son Sünnet'dir")
        print("İkindi Namazı toplam 8 rekatdır. 4 Rekat Sünnet ve 4 Rekat Farz'dır.")
        print("Akşam Namazı toplam 5 rekatdır. 3 Rekat Farz ve 2 Rekat Sünnetdir.")
        print("Yatsı Namazı toplam 14 rekatdır. 4 Rekat Sünnet, 4 Rekat Farz, 2 Rekat Son Sünnet ve 3 Rekat Vitir Vacip'tir.")
        print("\nEk bilgi: Tüm namazlarda ilk rekatta kıyamda Sübhaneke Duası okunur; oturuşlarda Tahiyyat, Allahümme Salli-Barik ve Rabbena Duaları okunur.")
        
        input("\nAna menüye dönmek için ENTER tuşuna basın...")
        continue # Döngünün başına (Ana Menüye) geri döndürür
        
    elif secim == "2":
        # Sure seçimi için iç döngü başlatıyoruz, böylece kullanıcı üst üste sure bakabilir
        while True:
            print("\n--- SURE LİSTESİ ---")
            sure_isimleri = list(sureler.keys())
            for sira, isim in enumerate(sure_isimleri, 1):
                print(f"{sira} - {isim}")
            print("0 - Ana Menüye Dön")
            
            try:
                sure_secim = int(input("\nİncelemek istediğiniz surenin numarasını giriniz: "))
                
                if sure_secim == 0:
                    break # İç döngüden çıkar, ana menüye döner
                    
                if 1 <= sure_secim <= len(sure_isimleri):
                    secilen_sure_adi = sure_isimleri[sure_secim - 1]
                    
                    print(f"\n=================== {secilen_sure_adi.upper()} ===================")
                    print(f"\n[OKUNUŞU]:\n{sureler[secilen_sure_adi]['okunus']}\n")
                    print(f"[ANLAMI]:\n{sureler[secilen_sure_adi]['anlam']}")
                    print("==================================================")
                    
                    input("\nDiğer sureleri görmek için ENTER tuşuna basın...")
                else:
                    print("\n[!] Geçersiz bir numara girdiniz. Lütfen listedeki sayılardan seçin.")
            except ValueError:
                print("\n[!] Lütfen sadece sayı giriniz.")
                
    elif secim == "3":
        print("\nDualar bölümü yakında eklenecektir!")
        input("\nAna menüye dönmek için ENTER tuşuna basın...")
        
    elif secim == "4":
        print("\nEsmaül Hüsna bölümü yakında eklenecektir!")
        input("\nAna menüye dönmek için ENTER tuşuna basın...")
        
    elif secim == "0":
        print("\nPenguen sizinle öğrenmekten çok mutlu oldu! Kendinize iyi bakın, Allah'a emanet olun. Elveda! 🐧")
        break # Ana döngüyü kırar ve programı tamamen sonlandırır
        
    else:
        print("\n[!] Geçersiz bir seçim yaptınız. Lütfen ana menüdeki numaralardan birini giriniz.")